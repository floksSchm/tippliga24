					<h3>2.9. Lizenzentzug</h3>
					Wurde einer Mannschaft die Lizenz entzogen, wird ihr Zugang gesperrt. Alle Paarungen (auch die zurückliegenden) werden als nicht getippt gewertet. Die Mannschaft wird in der Tabelle als Letzter mit 0 Punkten und 0:x Toren geführt. Die Tipppaarungen werden für die gegnerischen Mannschaften (auch rückwirkend) analog zu 2.8 gewertet. Nach Saisonende (Lizenzentzug Liga 1-3) oder sogar noch während der laufenden Runde (Lizenzentzug Liga 4) wird die Lücke durch eine Mannschaft aus der Qualifikationsliga gefüllt.<br /><br />

					Ein Lizenzentzug kann Änderungen in der Auf- /Abstiegsregelung mit sich ziehen.

