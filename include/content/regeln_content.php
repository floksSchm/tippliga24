					<h2>5. Sonderfälle</h2>
					<h3>5.1 Spielausfälle / Entscheidungen am grünen Tisch</h3>
					Ausgefallene Bundesliga-Spiele (inklusive Nachholspiele) werden nicht gewertet. Nachträglich am grünen Tisch getroffene Entscheidungen werden ebenfalls nicht berücksichtigt. Spielt z.B. der 1. FC Köln gegen Hansa Rostock und gewinnt mit 2:1, und im nachhinein wird das Spiel aufgrund eines Wechselfehlers der Kölner mit 0:2 gewertet, so bleibt es beim ursprünglichen Ergebnis von 2:1!

					<h3>5.2 Sonstiges</h3>
					Werden irgendwelche Pokal- oder Entscheidungsspiele (DFP-Pokal, UEFA-Cup, Champions-League) getippt, gilt das Ergebnis nach der regulären Spielzeit. Eine eventuelle Verlängerung oder ein Elfmeterschiessen muss nicht getippt werden!

					<h3>5.3 Sonstiges</h3>
					Entscheidungen zu Sonderfällen, die nicht aufgrund der Tippliga24-Regeln geregelt werden können, trifft der TL24-Ligaausschuss bei einem kühlen Weizen.

