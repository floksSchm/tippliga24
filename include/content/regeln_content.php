					<h2>4. Der Pokal</h2>
					Gespielt wird im K.O.-System. Es werden alle Runden ausgelost. Um einen geordneten Turnierbaum zu erhalten können vor einer Pokal-Runde Freilose verteilt werden. Die Freilos-Teams sind automatisch für die nächste Pokalrunde qualifiziert.

					<h3>4.1 Das TL24-Pokalgesetz</h3>
					Kommt es bei einer Tippbegegnung zu einem Unentschieden wird in nachfolgender Reihenfolge eine Entscheidung herbeigeführt:
					Verlängerung: Besser getippte Tordifferenzen (Bei 2:1-Endergebnis ist 1:0 besser als 2:0). Da bei Unentschieden keine unterschiedlichen Tordifferenzen möglich sind gibt es hierfür auch keine Tipptendenz-Punkte.<br />
					Elfmeterschießen: Bisherige Saisonleistung im Pokal (Anzahl Tore geteilt durch Spiele)<br />
					Sollte das Spiel dann immer noch nicht entschieden sein, wird die aktuelle Position in der Tippschützenliste herangezogen. Falls das immer noch zu keiner Entscheidung führt (was nahezu unmöglich ist) kommt der schlechter Platzierte weiter (zuerste Liga, dann Tabellenplatz). Und dann entscheidet zwangsläufig das Los...

					<h3>4.2 Nichttipper im Pokal</h3>
					Tippt ein Team im Pokal nicht, so kommt logischerweise das gegnerische Team weiter. Tippen beide Teams einer Tippbegebnung nicht scheiden beide aus und dafür kommt ein Verlierer aus den anderen Begegnungen dieser Runde weiter. Falls einige Begegnungen unentschieden endeten, haben diese "Verlierer-Teams" Vorrang. Entscheidungsreihenfolge siehe 4.1. Also zuerst bessere Leistung des aktuellen Pokalspieltags, dann gesamte Pokalrunde und letztlich die Tippschützenliste.<br />
					Eine Bestrafung von Nichttippern im Pokal durch gelbe Karten erfolgt nicht (ausser dem Ausscheiden)! Die TL24 behält sich jedoch vor offensichtliches Desinteresse mit einem Tippliga-Ausschluß zu ahnden. Dies kann besonders zu Beginn der Saison der Fall sein...


