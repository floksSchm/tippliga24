					<h2>2. Das Spiel</h2>
					<h3>2.1 Allgemein</h3>
					Ihr müsst an jedem Bundesliga-Spieltag alle Ergebnisse tippen. Zusätzlich tippt ihr noch gleichzeitig an jedem Tippspieltag gegen ein anderes Tippteam. Für die Tipps erhaltet Ihr unterschiedlich viele Tore. Habt ihr mehr Tore erzielt wie euer Tippgegener, gewinnt ihr und bekommt 3 Punkte - bei Unentschieden einen Punkt. Mit vielen Punkten werdet Ihr Meister Eurer Liga und steigt damit auf und/oder fahrt "saftige Gewinne" ein. Außerdem gibt es interessante Statistiken über die aktuellen Stände, die Ihr Euch jederzeit anschauen könnt.<br /><br />

					Tippende ist immer eine halbe Stunde vor Spielbeginn. Somit kann das Freitagsspiel der Fußball-Bundesliga das um 20.30 Uhr beginnt bis um 20.00 Uhr getippt werden.

					<h3>2.2 Die Ligen / Verlauf</h3>
					Die sich während einer laufenden Saison anmelden, werden einer Qualifikationsliga zugeteilt. Nachdem Ihr in einer Liga zugeteilt worden seid, müsst Ihr die Spieltage tippen. Für ein identisches Ergebnis erhaltet Ihr 3 Punkte, wenn Ihr Sieg, Niederlage oder Unentschieden richtig getippt habt, 1 Punkt. Diese Punkte sind für das "Torverhältnis". Diese Punkte werden ab dieser Stelle, als Tore bezeichnet. Ihr spielt zudem jeden Spieltag gegen eine andere Mannschaft euerer Liga. Wer mehr Tore an diesem Spieltag ergattert hat, bekommt 3 Punkte, bei Unentschieden jeder 1 Punkt.

