					<h3>2.3 Die Qualifikationsliga</h3>
					Alle Neueinsteiger werden während der laufenden Runde der Qualifikationsliga zugeordnet. In der Qualifikationsliga tippt jede Mannschaft für sich selbst. Es wird also nicht wie in den anderen Ligen gegeneinander getippt. Die Punkteverteilung bleibt gleich: 3 Punkte bei richtigem Ergebnis, 1 Punkt bei richtiger Tendenz. Je mehr Punkte eine Mannschaft hat, umso höher steht sie in der Tabelle. Sobald eine Mannschaft aus eine der vier vierten Ligen rausfällt, wird diese (auch während) der laufenden Runde aus der Qualifikationsliga nach dem Tabellenstand aufgefüllt. Nach Beendigung einer Runde werden zusätzlich alle freien Plätze in den oberen Ligen durch die Qualiliga aufgefüllt: Sollte es mehr Aufsteiger aus den 4. Ligen geben, wird der freigewordene Platz wird durch Qualifikanten ersetzt. Wenn sich nach Ende der Saison jemand abmeldet oder jemand die Lizenz entzogen wurde (betrifft Teams in der dritten Liga oder höher), gibt es auch zusätzliche Plätze für Nachrücker aus der Qualifikationsliga.

					<strong>Achtung:</strong> Es ist kein Abstieg aus den vierten Ligen möglich. Wer einmal oben spielt, ist immer dabei - ausser ihm wird die Lizenz entzogen. So ist also die einzigste Chance für einen Quali-Ligisten in die reguläre TL24 zu kommen, dass ein Tipper von oben rausfällt.<br /><br />

					Am 02. September 2009 wurde in der Qualifikationsliga der QualiKalla-Modus eingeführt, nach dem die Punkte und Dreier der abgelaufenen Saison auf die neue Saison übertragen werden. Damit sollen motivierte Langzeittipper in der Qualifikationsliga besser berücksichtigt werden!<br /><br />

					Mit dem Start zur 18. Saison am 05. März 2010 wurde dieser um die QualiKallamil-Regel ergänzt, die besagt, dass die Punkte lediglich über zwei Spielrunden weitergeführt werden, um den Anreiz für Neueinsteiger nicht ganz zu versauen.

					<h3>2.4 Auf- und Abstieg </h3>
					Die Anzahl der Auf- und Absteiger werden zu Beginn einer Saison festgelegt. Sie können sich aber aus bestimmten Gründen (Neueinteilung der Ligen, Neugründung einer Liga,...) noch während einer Runde ändern. Die jeweiligen Auf- und Abstiegsplätze sind in den Tabellen farblich gekennzeichnet. In der ersten Liga gibt es keine Aufsteiger und in den 4. Ligen logischerweise keine Absteiger.

